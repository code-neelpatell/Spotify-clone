// Function to render playlists based on screen size
function renderPlaylists() {
    let screenWidth = window.innerWidth;
    let numCardsToShow;

    // Determine number of cards to show based on screen width
    if (screenWidth > 1024) {
        numCardsToShow = 7; // Desktop and large screens
    } else if (screenWidth <= 1024 && screenWidth > 600) {
        numCardsToShow = 4; // Tablets
    } else {
        numCardsToShow = 2; // Mobile devices
    }

    let playlistContainer = document.getElementById('feed');
    playlistContainer.innerHTML = ''; // Clear existing content

    // Loop through playlists and create HTML elements
    playlists.forEach(playlist => {
        let feedGrid =
            `<div class="playlist">
                <div class="grid-header flex">
                    <a href="#" class="text-heading font-bolder color-highlight">${playlist.name}</a>
                    <a href="#" class="font-bold no-highlight">Show all</a>
                </div>
                <div class="grid flex" >`

        // Add cards to the playlist
        playlist.cards.slice(0, numCardsToShow).forEach(card => {
            feedGrid +=
                `<div class="card card-feed ${playlist.cardType}">
                    <div class="preview">
                        <img src="/assets/images/feed/${card.imageSrc}" alt="card thumbnail">
                            <button class="icon large effect-none effect-hover-scale">
                                <img src="/assets/images/icons/play.svg" alt="">
                            </button>
                    </div>
                    <div class="text">
                        <h4>${card.title}</h4>
                        <p class='${playlist.descType}'>
                        `;
            if (playlist.descType.search('list') !== -1) {
                card.desc.forEach(item =>
                    feedGrid += `<span><a class="no-highlight">${item}</a></span>`
                )
            }
            else feedGrid += card.desc;
            feedGrid += `</div></div>`;
        });
        feedGrid += `</p></div></div >`
        feed.innerHTML += feedGrid;
    });
}

// Initial render on page load
renderPlaylists();

// Re-render on window resize (optional)
window.addEventListener('resize', renderPlaylists);